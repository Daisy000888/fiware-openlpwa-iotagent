package com.orange.fiware.openlpwa.provider;

/**
 *  Error returned by the OpenLpwa provider API
 */
public class OpenLpwaProviderError {
    
    private String id;
    private int code;
    private String message;

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public int getCode() {
        return code;
    }

    public void setCode(int code) {
        this.code = code;
    }

    public String getMessage() {
        return message;
    }

    public void setMessage(String message) {
        this.message = message;
    }
}
